# Есть зашифрованное сообщение
secret_message = [
    'квевтфпп6щ3стмзалтнмаршгб5длгуча',
    'дьсеы6лц2бане4т64ь4б3ущея6втщл6б',
    'т3пплвце1н3и2кд4лы12чф1ап3бкычаь',
    'ьд5фму3ежородт9г686буиимыкучшсал',
    'бсц59мегщ2лятьаьгенедыв9фк9ехб1а',
]

def decrypt():
    """Расшифровка секретного сообщения"""
    # Первое слово - 4-я буква (индекс 3, так как отсчёт с 1)
    word1 = secret_message[0][3]

    # Второе слово - буквы с 10 по 13 включительно (индексы 9:13)
    word2 = secret_message[1][9:13]

    # Третье слово - буквы с 6 по 15 через одну (индексы 5:15:2)
    word3 = secret_message[2][5:15:2]

    # Четвертое слово - буквы с 8 по 13 в обратном порядке (индексы 12:6:-1)
    word4 = secret_message[3][12:6:-1]

    # Пятое слово - буквы с 17 по 21 в обратном порядке (индексы 20:15:-1)
    word5 = secret_message[4][20:15:-1]

    decoded_message = f"{word1} {word2} {word3} {word4} {word5}"
    return decoded_message
